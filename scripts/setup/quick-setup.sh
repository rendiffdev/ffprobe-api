echo ""

# Check Docker
if ! command -v docker >/dev/null 2>&1; then
    error "Docker is required but not installed. Please install Docker first."
fi

if ! command -v "docker compose" >/dev/null 2>&1; then
    error "Docker Compose v2 is required. Please update Docker."
fi

log "Docker and Docker Compose found âœ…"

# Quick configuration
echo ""
info "ðŸ”§ Quick Configuration Setup"
echo ""

# Setup mode
echo "Select setup mode:"
echo "1. ðŸ”§ Development (Local development, no auth, debug logging)"
echo "2. ðŸ§ª Demo (Demo mode with basic auth, sample data)"
echo "3. ðŸ­ Production (Full security, SSL ready)"
echo ""

while true; do
    read -p "Choose mode (1-3): " mode_choice
    case $mode_choice in
        1)
            SETUP_MODE="development"
            break
            ;;
        2)
            SETUP_MODE="demo"
            break
            ;;
        3)
            SETUP_MODE="production"
            break
            ;;
        *)
            warn "Please choose 1, 2, or 3"
            ;;
    esac
done

log "Selected: $SETUP_MODE mode"

# Generate secrets
log "Generating secure secrets..."
API_KEY=$(openssl rand -base64 32 | tr -d "=+/" | cut -c1-32)
JWT_SECRET=$(openssl rand -base64 32 | tr -d "=+/" | cut -c1-32)
DB_PASSWORD=$(openssl rand -base64 16 | tr -d "=+/" | cut -c1-16)
REDIS_PASSWORD=$(openssl rand -base64 16 | tr -d "=+/" | cut -c1-16)
GRAFANA_PASSWORD=$(openssl rand -base64 12 | tr -d "=+/" | cut -c1-12)

# Create environment file
log "Creating environment configuration..."

case $SETUP_MODE in
    "development")
        cat > .env << EOF
# FFprobe API Development Configuration
# Generated by quick setup on $(date)

# Development Mode Settings
ENVIRONMENT=development
LOG_LEVEL=debug
ENABLE_AUTH=false
ENABLE_RATE_LIMIT=false
ENABLE_CSRF=false

# Network
API_PORT=8080

# Security (for development)
API_KEY=$API_KEY
JWT_SECRET=$JWT_SECRET

# Database
POSTGRES_PASSWORD=$DB_PASSWORD
POSTGRES_USER=ffprobe
POSTGRES_DB=ffprobe_api

# Redis
REDIS_PASSWORD=$REDIS_PASSWORD

# Monitoring
GRAFANA_PASSWORD=$GRAFANA_PASSWORD
GRAFANA_USER=admin

# File Storage
DATA_PATH=./data
MAX_FILE_SIZE=53687091200
MAX_CONCURRENT_JOBS=4
EOF
        ;;
        
    "demo")
        cat > .env << EOF
# FFprobe API Demo Configuration
# Generated by quick setup on $(date)

# Demo Mode Settings
ENVIRONMENT=staging
LOG_LEVEL=info
ENABLE_AUTH=true
ENABLE_RATE_LIMIT=true
ENABLE_CSRF=false

# Network
API_PORT=8080

# Security
API_KEY=$API_KEY
JWT_SECRET=$JWT_SECRET

# Database
POSTGRES_PASSWORD=$DB_PASSWORD
POSTGRES_USER=ffprobe
POSTGRES_DB=ffprobe_api

# Redis
REDIS_PASSWORD=$REDIS_PASSWORD

# Rate Limiting
RATE_LIMIT_PER_MINUTE=100
RATE_LIMIT_PER_HOUR=2000
RATE_LIMIT_PER_DAY=20000

# Monitoring
GRAFANA_PASSWORD=$GRAFANA_PASSWORD
GRAFANA_USER=admin

# File Storage
DATA_PATH=./data
MAX_FILE_SIZE=53687091200
MAX_CONCURRENT_JOBS=4
EOF
        ;;
        
    "production")
        echo ""
        read -p "ðŸŒ Enter your domain name (e.g., api.yourcompany.com): " DOMAIN_NAME
        read -p "ðŸ“§ Enter email for SSL certificates: " EMAIL_ADDRESS
        
        cat > .env << EOF
# FFprobe API Production Configuration
# Generated by quick setup on $(date)

# Production Mode Settings
ENVIRONMENT=production
LOG_LEVEL=warn
ENABLE_AUTH=true
ENABLE_RATE_LIMIT=true
ENABLE_CSRF=true

# Network
API_PORT=8080
DOMAIN_NAME=$DOMAIN_NAME
HTTP_PORT=80
HTTPS_PORT=443

# Security
API_KEY=$API_KEY
JWT_SECRET=$JWT_SECRET

# Database
POSTGRES_PASSWORD=$DB_PASSWORD
POSTGRES_USER=ffprobe
POSTGRES_DB=ffprobe_api

# Redis
REDIS_PASSWORD=$REDIS_PASSWORD

# Rate Limiting
RATE_LIMIT_PER_MINUTE=60
RATE_LIMIT_PER_HOUR=1000
RATE_LIMIT_PER_DAY=10000

# Monitoring
GRAFANA_PASSWORD=$GRAFANA_PASSWORD
GRAFANA_USER=admin

# File Storage
DATA_PATH=./data
MAX_FILE_SIZE=53687091200
MAX_CONCURRENT_JOBS=4

# SSL Configuration
EMAIL=$EMAIL_ADDRESS
EOF
        ;;
esac

# Create data directories
log "Creating data directories..."
mkdir -p data/{postgres,redis,uploads,reports,models,logs,temp,cache,prometheus,grafana,backup}

# Set permissions
chmod 600 .env
chmod -R 755 data/

# Start services
log "Starting FFprobe API services..."

case $SETUP_MODE in
    "development")
        info "Starting in development mode..."
        docker compose -f compose.yml -f compose.dev.yml up -d --build
        ;;
    "demo"|"production")
        info "Starting in production mode..."
        docker compose -f compose.yml -f compose.prod.yml up -d --build
        ;;
esac

# Wait for services
log "Waiting for services to start..."
sleep 30

# Health check
info "Checking service health..."
if curl -f -s http://localhost:8080/health >/dev/null 2>&1; then
    log "âœ… FFprobe API is running!"
else
    warn "âš ï¸  API may still be starting. Check with: docker compose logs ffprobe-api"
fi

# Show access information
echo ""
echo "â•”â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•—"
echo "â•‘                          ðŸŽ‰ Setup Complete!                                 â•‘"
echo "â•šâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
